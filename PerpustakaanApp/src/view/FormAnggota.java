    private void tampilData() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID");      // Kolom 0: ID (Akan disembunyikan)
        model.addColumn("NO");      // Kolom 1: No Urut
        model.addColumn("Nama");    // Kolom 2: Nama
        model.addColumn("Alamat");  // Kolom 3: Alamat
        model.addColumn("Telepon"); // Kolom 4: Telepon

        try {
            Connection conn = KoneksiDB.getConnection();
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM anggota");

            int no = 1;
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("id_anggota"), // Ambil ID dari database
                    no++,
                    rs.getString("nama"),
                    rs.getString("alamat"),
                    rs.getString("telepon")
                });
            }

            jTable1.setModel(model);

            // --- 1. SEMBUNYIKAN KOLOM ID (Kolom ke-0) ---
            jTable1.getColumnModel().getColumn(0).setMinWidth(0);
            jTable1.getColumnModel().getColumn(0).setMaxWidth(0);
            jTable1.getColumnModel().getColumn(0).setWidth(0);

            // --- 2. SETTING WARNA HEADER (BIRU) ---
            javax.swing.table.DefaultTableCellRenderer headerRenderer = new javax.swing.table.DefaultTableCellRenderer();
            headerRenderer.setBackground(new java.awt.Color(37, 99, 235)); // Biru Royal
            headerRenderer.setForeground(java.awt.Color.WHITE);             // Teks Putih
            headerRenderer.setFont(new java.awt.Font("Segoe UI", java.awt.Font.BOLD, 12));
            headerRenderer.setHorizontalAlignment(javax.swing.JLabel.CENTER);

            for (int i = 0; i < jTable1.getColumnModel().getColumnCount(); i++) {
                jTable1.getColumnModel().getColumn(i).setHeaderRenderer(headerRenderer);
            }

            // --- 3. SETTING WARNA BODY TABEL (DARK) ---
            jTable1.setBackground(new java.awt.Color(43, 48, 59));
            jTable1.setForeground(java.awt.Color.WHITE);
            jTable1.setGridColor(new java.awt.Color(60, 65, 75));

        } catch (Exception e) {
            System.out.println("Gagal tampil data: " + e.getMessage());
        }
    }
    private void resetForm() {
        txtNama.setText("");
        txtAlamat.setText("");
        txtTelepon.setText("");
        idAnggota = 0;
    }

