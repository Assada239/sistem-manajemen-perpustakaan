    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int row = jTable1.getSelectedRow();
        
        // Pastikan baris terpilih valid
        if (row != -1) {
            // Ambil ID dari kolom 0 (yang tersembunyi tadi)
            idAnggota = Integer.parseInt(jTable1.getModel().getValueAt(row, 0).toString());
            
            // Masukkan data ke TextField
            txtNama.setText(jTable1.getValueAt(row, 2).toString());   // Kolom 2: Nama
            txtAlamat.setText(jTable1.getValueAt(row, 3).toString()); // Kolom 3: Alamat
            txtTelepon.setText(jTable1.getValueAt(row, 4).toString());// Kolom 4: Telepon
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        try {
            Connection conn = KoneksiDB.getConnection();
            Statement st = conn.createStatement();

            st.executeUpdate("INSERT INTO anggota (nama, alamat, telepon) VALUES ("
                    + "'" + txtNama.getText() + "',"
                    + "'" + txtAlamat.getText() + "',"
                    + "'" + txtTelepon.getText() + "')");

            JOptionPane.showMessageDialog(this, "Data anggota berhasil disimpan");
            
            tampilData(); // Refresh tabel
            resetForm();  // Bersihkan form (Pakai method mu)

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal menyimpan data");
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        if (idAnggota == 0) {
            JOptionPane.showMessageDialog(this, "Pilih data terlebih dahulu");
            return;
        }

        try {
            Connection conn = KoneksiDB.getConnection();
            Statement st = conn.createStatement();

            st.executeUpdate("UPDATE anggota SET "
                    + "nama='" + txtNama.getText() + "', "
                    + "alamat='" + txtAlamat.getText() + "', "
                    + "telepon='" + txtTelepon.getText() + "' "
                    + "WHERE id_anggota=" + idAnggota);

            JOptionPane.showMessageDialog(this, "Data berhasil diupdate");

            tampilData();
            resetForm(); // Reset ID jadi 0 dan bersihkan text

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal update data");
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
         if (idAnggota == 0) {
            JOptionPane.showMessageDialog(this, "Pilih data terlebih dahulu");
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(this,
            "Yakin ingin menghapus data?", "Konfirmasi", JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            try {
                Connection conn = KoneksiDB.getConnection();
                Statement st = conn.createStatement();

                st.executeUpdate("DELETE FROM anggota WHERE id_anggota=" + idAnggota);

                JOptionPane.showMessageDialog(this, "Data berhasil dihapus");

                tampilData();
                resetForm(); // Bersihkan

            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Gagal menghapus data");
            }
        }
    }//GEN-LAST:event_btnHapusActionPerformed

