    private void tampilData() {
        DefaultTableModel model = new DefaultTableModel();
    model.addColumn("NO");
    model.addColumn("Judul");
    model.addColumn("Penulis");
    model.addColumn("Tahun");
    model.addColumn("Stok");

        try {
            Connection conn = KoneksiDB.getConnection();
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM buku");

            int no = 1;
            while (rs.next()) {
                model.addRow(new Object[]{
                    no++,
                    rs.getString("judul"),
                    rs.getString("penulis"),
                    rs.getInt("tahun"),
                    rs.getInt("stok")
                });
            }

            // 1. Pasang Model Data Baru
            jTable1.setModel(model);

            // 2. SETELAH DATA MASUK, PASANG ULANG WARNANYA DI SINI
            // --- Mulai Kodingan Warna ---
            javax.swing.table.DefaultTableCellRenderer headerRenderer = new javax.swing.table.DefaultTableCellRenderer();
            headerRenderer.setBackground(new java.awt.Color(37, 99, 235)); // Biru Royal
            headerRenderer.setForeground(java.awt.Color.WHITE);             // Teks Putih
            headerRenderer.setFont(new java.awt.Font("Segoe UI", java.awt.Font.BOLD, 12));
            headerRenderer.setHorizontalAlignment(javax.swing.JLabel.CENTER);

            // Terapkan ke setiap kolom yang baru dibuat
            for (int i = 0; i < jTable1.getColumnModel().getColumnCount(); i++) {
                jTable1.getColumnModel().getColumn(i).setHeaderRenderer(headerRenderer);
            }

            // Warna Body Tabel
            jTable1.setBackground(new java.awt.Color(43, 48, 59));
            jTable1.setForeground(java.awt.Color.WHITE);
            jTable1.setGridColor(new java.awt.Color(60, 65, 75));
            // --- Selesai Kodingan Warna ---

        } catch (Exception e) {
            System.out.println("Gagal tampil data: " + e.getMessage());
        }
    }
