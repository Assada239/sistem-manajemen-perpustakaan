    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        try {
        if (txtJudul.getText().isEmpty() ||
            txtPenulis.getText().isEmpty() ||
            txtPenerbit.getText().isEmpty() ||
            txtTahun.getText().isEmpty() ||
            txtStok.getText().isEmpty()) {

            JOptionPane.showMessageDialog(this, "Semua field harus diisi!");
            return;
        }

        BukuController bc = new BukuController();
        bc.simpan(
            txtJudul.getText(),
            txtPenulis.getText(),
            txtPenerbit.getText(),
            Integer.parseInt(txtTahun.getText()),
            Integer.parseInt(txtStok.getText())
        );

        JOptionPane.showMessageDialog(this, "Data buku berhasil disimpan");

        kosongkanForm();
        tampilData();

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Tahun dan Stok harus angka!");
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal menyimpan data");
        System.out.println(e.getMessage());
    }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int row = jTable1.getSelectedRow();

    txtJudul.setText(jTable1.getValueAt(row, 1).toString());
    txtPenulis.setText(jTable1.getValueAt(row, 2).toString());
    txtTahun.setText(jTable1.getValueAt(row, 3).toString());
    txtStok.setText(jTable1.getValueAt(row, 4).toString());

    try {
        Connection conn = KoneksiDB.getConnection();
        Statement st = conn.createStatement();

        ResultSet rs = st.executeQuery(
            "SELECT id_buku FROM buku WHERE judul='" + txtJudul.getText() + "'"
        );

        if (rs.next()) {
            idBuku = rs.getInt("id_buku");
        }

    } catch (Exception e) {
        System.out.println("Gagal ambil ID: " + e.getMessage());
    }
    }//GEN-LAST:event_jTable1MouseClicked

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
         if (idBuku == 0) {
        JOptionPane.showMessageDialog(this, "Pilih data terlebih dahulu!");
        return;
    }

    try {
        Connection conn = KoneksiDB.getConnection();
        Statement st = conn.createStatement();

        st.executeUpdate("UPDATE buku SET "
                + "judul='" + txtJudul.getText() + "', "
                + "penulis='" + txtPenulis.getText() + "', "
                + "tahun=" + txtTahun.getText() + ", "
                + "stok=" + txtStok.getText()
                + " WHERE id_buku=" + idBuku);

        JOptionPane.showMessageDialog(this, "Data berhasil diupdate");

        kosongkanForm();
        tampilData();
        idBuku = 0;

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal update data");
        System.out.println(e.getMessage());
    }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        if (idBuku == 0) {
        JOptionPane.showMessageDialog(this, "Pilih data terlebih dahulu!");
        return;
    }

    int confirm = JOptionPane.showConfirmDialog(
        this,
        "Yakin ingin menghapus data?",
        "Konfirmasi",
        JOptionPane.YES_NO_OPTION
    );

    if (confirm == JOptionPane.YES_OPTION) {
        try {
            Connection conn = KoneksiDB.getConnection();
            Statement st = conn.createStatement();

            st.executeUpdate("DELETE FROM buku WHERE id_buku=" + idBuku);

            JOptionPane.showMessageDialog(this, "Data berhasil dihapus");

            kosongkanForm();
            tampilData();
            idBuku = 0;

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal menghapus data");
            System.out.println(e.getMessage());
        }
    }
    }//GEN-LAST:event_btnHapusActionPerformed

