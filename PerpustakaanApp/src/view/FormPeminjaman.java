    private void btnPinjamActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPinjamActionPerformed
        // TODO add your handling code here:
        if (cmbAnggota.getSelectedIndex() == -1 ||
        cmbBuku.getSelectedIndex() == -1) {
        JOptionPane.showMessageDialog(this, "Data belum lengkap");
        return;
    }

    String anggota = cmbAnggota.getSelectedItem().toString();
    String buku = cmbBuku.getSelectedItem().toString();

    int idAnggota = Integer.parseInt(anggota.split(" - ")[0]);
    int idBuku = Integer.parseInt(buku.split(" - ")[0]);

    try {
        Connection conn = KoneksiDB.getConnection();
        Statement st = conn.createStatement();

        // INSERT PEMINJAMAN
        st.executeUpdate(
            "INSERT INTO peminjaman (id_anggota, id_buku, tgl_pinjam, status) VALUES (" +
            idAnggota + "," +
            idBuku + "," +
            "'" + txtTanggalPinjam.getText() + "'," +
            "'Dipinjam')"
        );

        // KURANGI STOK
        st.executeUpdate(
            "UPDATE buku SET stok = stok - 1 WHERE id_buku=" + idBuku
        );

        JOptionPane.showMessageDialog(this, "Peminjaman berhasil");

        loadBuku();
        tampilData();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal meminjam");
    }
    }//GEN-LAST:event_btnPinjamActionPerformed

