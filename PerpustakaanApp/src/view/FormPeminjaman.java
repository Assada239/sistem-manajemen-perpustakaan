    private void loadAnggota() {
        try {
            Connection conn = KoneksiDB.getConnection();
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM anggota");

            cmbAnggota.removeAllItems();

            while (rs.next()) {
                cmbAnggota.addItem(
                    rs.getInt("id_anggota") + " - " + rs.getString("nama")
                );
            }

        } catch (Exception e) {
            System.out.println("Gagal load anggota");
        }
    }
    
    private void loadBuku() {
        try {
            Connection conn = KoneksiDB.getConnection();
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(
                "SELECT * FROM buku WHERE stok > 0"
            );

            cmbBuku.removeAllItems();

            while (rs.next()) {
                cmbBuku.addItem(
                    rs.getInt("id_buku") + " - " +
                    rs.getString("judul") + " (Stok: " + rs.getInt("stok") + ")"
                );
            }

        } catch (Exception e) {
            System.out.println("Gagal load buku");
        }
    }
    
    private void setTanggalPinjam() {
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        txtTanggalPinjam.setText(sdf.format(new Date()));
        txtTanggalPinjam.setEditable(true);
    }
    
    private void tampilData() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("NO");
        model.addColumn("Anggota");
        model.addColumn("Buku");
        model.addColumn("Status");

        try {
            Connection conn = KoneksiDB.getConnection();
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(
                "SELECT p.*, a.nama, b.judul FROM peminjaman p " +
                "JOIN anggota a ON p.id_anggota = a.id_anggota " +
                "JOIN buku b ON p.id_buku = b.id_buku"
            );

            int no = 1;
            while (rs.next()) {
                model.addRow(new Object[]{
                    no++,
                    rs.getString("nama"),
                    rs.getString("judul"),
                    rs.getString("status")
                });
            }

            jTable1.setModel(model);
            javax.swing.table.DefaultTableCellRenderer headerRenderer = new javax.swing.table.DefaultTableCellRenderer();
            headerRenderer.setBackground(new java.awt.Color(37, 99, 235));  
            headerRenderer.setForeground(java.awt.Color.WHITE);              
            headerRenderer.setFont(new java.awt.Font("Segoe UI", java.awt.Font.BOLD, 12));
            headerRenderer.setHorizontalAlignment(javax.swing.JLabel.CENTER); 

            for (int i = 0; i < jTable1.getColumnModel().getColumnCount(); i++) {
                jTable1.getColumnModel().getColumn(i).setHeaderRenderer(headerRenderer);
            }

            jTable1.setBackground(new java.awt.Color(43, 48, 59)); 
            jTable1.setForeground(java.awt.Color.WHITE);           
            jTable1.setGridColor(new java.awt.Color(60, 65, 75));  
            jTable1.setRowHeight(25);                              
            jTable1.setShowGrid(true);                             

        } catch (Exception e) {
            System.out.println("Gagal tampil peminjaman");
        }
    }

    
    




