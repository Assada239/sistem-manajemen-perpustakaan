    private void tampilData() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID");
        model.addColumn("Anggota");
        model.addColumn("Buku");
        model.addColumn("Tgl Pinjam");

        try {
            Connection conn = KoneksiDB.getConnection();
            Statement st = conn.createStatement();

            String sql =
                "SELECT p.id_pinjam, a.nama, b.judul, p.tgl_pinjam " +
                "FROM peminjaman p " +
                "JOIN anggota a ON p.id_anggota = a.id_anggota " +
                "JOIN buku b ON p.id_buku = b.id_buku " +
                "WHERE p.status = 'Dipinjam'";

            ResultSet rs = st.executeQuery(sql);

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("id_pinjam"),
                    rs.getString("nama"),
                    rs.getString("judul"),
                    rs.getString("tgl_pinjam")
                });
            }

            jTable1.setModel(model);

            // --- MULAI KODINGAN DESAIN TABEL (Agar Biru & Gelap) ---
            
            // 1. Atur Warna Header (Judul Kolom jadi Biru)
            javax.swing.table.DefaultTableCellRenderer headerRenderer = new javax.swing.table.DefaultTableCellRenderer();
            headerRenderer.setBackground(new java.awt.Color(37, 99, 235));  // Warna Biru Royal
            headerRenderer.setForeground(java.awt.Color.WHITE);              // Teks Putih
            headerRenderer.setFont(new java.awt.Font("Segoe UI", java.awt.Font.BOLD, 12));
            headerRenderer.setHorizontalAlignment(javax.swing.JLabel.CENTER); // Teks Rata Tengah

            // Terapkan ke setiap kolom yang ada
            for (int i = 0; i < jTable1.getColumnModel().getColumnCount(); i++) {
                jTable1.getColumnModel().getColumn(i).setHeaderRenderer(headerRenderer);
            }

            // 2. Atur Warna Body (Isi Tabel jadi Gelap)
            jTable1.setBackground(new java.awt.Color(43, 48, 59)); // Warna Latar Gelap
            jTable1.setForeground(java.awt.Color.WHITE);           // Teks Isi Putih
            jTable1.setGridColor(new java.awt.Color(60, 65, 75));  // Warna Garis Grid
            jTable1.setRowHeight(25);                              // Sedikit lebih tinggi biar rapi
            jTable1.setShowGrid(true);                             // Tampilkan garis grid
            
            // --- SELESAI KODINGAN DESAIN ---

        } catch (Exception e) {
            System.out.println("Gagal tampil data: " + e.getMessage());
        }
    }

    private void hitungDenda() {
        LocalDate hariIni = LocalDate.now();

        long lamaPinjam = ChronoUnit.DAYS.between(tglPinjam, hariIni);
        long telat = lamaPinjam - 7;

        int denda = 0;
        if (telat > 0) {
            denda = (int) telat * 1000;
        }

        txtDenda.setText(String.valueOf(denda));
    }



