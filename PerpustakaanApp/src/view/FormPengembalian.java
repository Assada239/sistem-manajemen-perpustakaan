    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int row = jTable1.getSelectedRow();

        idPinjam = Integer.parseInt(jTable1.getValueAt(row, 0).toString());
        tglPinjam = LocalDate.parse(jTable1.getValueAt(row, 3).toString());

        try {
            Connection conn = KoneksiDB.getConnection();
            Statement st = conn.createStatement();

            ResultSet rs = st.executeQuery(
                "SELECT id_buku FROM peminjaman WHERE id_pinjam=" + idPinjam
            );

            if (rs.next()) {
                idBuku = rs.getInt("id_buku");
            }

        } catch (Exception e) {
            System.out.println("Gagal ambil id buku");
        }

        hitungDenda();
    }//GEN-LAST:event_jTable1MouseClicked

    private void btnKembalikanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKembalikanActionPerformed
        // TODO add your handling code here:
        if (idPinjam == 0) {
            JOptionPane.showMessageDialog(this, "Pilih data terlebih dahulu");
            return;
        }

        try {
            Connection conn = KoneksiDB.getConnection();
            Statement st = conn.createStatement();

            st.executeUpdate(
                "UPDATE peminjaman SET " +
                "tgl_kembali = DATE('now'), " +
                "denda = " + txtDenda.getText() + ", " +
                "status = 'KEMBALI' " +
                "WHERE id_pinjam = " + idPinjam
            );

            st.executeUpdate(
                "UPDATE buku SET stok = stok + 1 WHERE id_buku = " + idBuku
            );

            JOptionPane.showMessageDialog(this, "Buku berhasil dikembalikan");

            txtDenda.setText("");
            idPinjam = 0;
            tampilData();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal mengembalikan buku");
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_btnKembalikanActionPerformed

